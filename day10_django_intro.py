# 🧠 What is Django?
# Django is a high-level Python web framework that helps you build secure, scalable web apps quickly.
# It follows the Model-View-Template (MVT) pattern.

# 📦 Basic concepts:
# Project: The overall configuration and settings.
# App: A specific module that handles one feature (like 'blog' or 'users').

# 🧱 Key commands:
#   django-admin startproject projectname
#   python manage.py startapp appname
#   python manage.py runserver

# ⚙️ Typical folder structure:
# myproject/
# ├── manage.py
# ├── myproject/
# │   ├── __init__.py
# │   ├── settings.py
# │   ├── urls.py
# │   └── wsgi.py
# └── appname/
#     ├── admin.py
#     ├── apps.py
#     ├── models.py
#     ├── views.py
#     └── templates/

